return {
  -- Debug Adapter Protocol (DAP) core
  {
    "mfussenegger/nvim-dap",
    dependencies = {
      -- UI for debugging
      "rcarriga/nvim-dap-ui",
      "nvim-neotest/nvim-nio",
      
      -- Virtual text during debugging
      "theHamsta/nvim-dap-virtual-text",
      
      -- Language specific adapters
      "mfussenegger/nvim-dap-python",
      "leoluz/nvim-dap-go",
    },
    config = function()
      local dap = require("dap")
      local dapui = require("dapui")
      
      -- Setup DAP UI
      dapui.setup({
        icons = { expanded = "‚ñæ", collapsed = "‚ñ∏", current_frame = "*" },
        controls = {
          icons = {
            pause = "‚è∏",
            play = "‚ñ∂",
            step_into = "‚èé",
            step_over = "‚è≠",
            step_out = "‚èÆ",
            step_back = "b",
            run_last = "‚ñ∂‚ñ∂",
            terminate = "‚èπ",
            disconnect = "‚èè",
          },
        },
      })
      
      -- Setup virtual text
      require("nvim-dap-virtual-text").setup()
      
      -- Auto open/close DAP UI
      dap.listeners.after.event_initialized["dapui_config"] = function()
        dapui.open()
      end
      dap.listeners.before.event_terminated["dapui_config"] = function()
        dapui.close()
      end
      dap.listeners.before.event_exited["dapui_config"] = function()
        dapui.close()
      end
      
      -- Debug signs
      vim.fn.sign_define("DapBreakpoint", { text = "üî¥", texthl = "", linehl = "", numhl = "" })
      vim.fn.sign_define("DapBreakpointCondition", { text = "üü°", texthl = "", linehl = "", numhl = "" })
      vim.fn.sign_define("DapLogPoint", { text = "üü¢", texthl = "", linehl = "", numhl = "" })
      vim.fn.sign_define("DapStopped", { text = "‚ñ∂Ô∏è", texthl = "", linehl = "DapStoppedLine", numhl = "" })
      
      -- Python debugging
      require("dap-python").setup("python")
      
      -- Go debugging
      require("dap-go").setup()
      
      -- JavaScript/TypeScript debugging (Node.js)
      dap.adapters.node2 = {
        type = "executable",
        command = "node",
        args = { vim.fn.stdpath("data") .. "/mason/packages/node-debug2-adapter/out/src/nodeDebug.js" },
      }
      
      dap.configurations.javascript = {
        {
          name = "Launch",
          type = "node2",
          request = "launch",
          program = "${file}",
          cwd = vim.fn.getcwd(),
          sourceMaps = true,
          protocol = "inspector",
          console = "integratedTerminal",
        },
        {
          name = "Attach to process",
          type = "node2",
          request = "attach",
          processId = require("dap.utils").pick_process,
        },
      }
      
      dap.configurations.typescript = dap.configurations.javascript
      
      -- Rust debugging
      dap.adapters.codelldb = {
        type = "server",
        port = "${port}",
        executable = {
          command = vim.fn.stdpath("data") .. "/mason/packages/codelldb/extension/adapter/codelldb",
          args = { "--port", "${port}" },
        }
      }
      
      dap.configurations.rust = {
        {
          name = "Launch",
          type = "codelldb",
          request = "launch",
          program = function()
            return vim.fn.input("Path to executable: ", vim.fn.getcwd() .. "/target/debug/", "file")
          end,
          cwd = "${workspaceFolder}",
          stopOnEntry = false,
        },
      }
      
      -- C/C++ debugging
      dap.configurations.c = dap.configurations.rust
      dap.configurations.cpp = dap.configurations.rust
      
      -- Java debugging (requires nvim-jdtls)
      dap.configurations.java = {
        {
          type = "java",
          request = "attach",
          name = "Debug (Attach) - Remote",
          hostName = "127.0.0.1",
          port = 5005,
        },
      }
    end,
  },
  
  -- Mason integration for debug adapters
  {
    "jay-babu/mason-nvim-dap.nvim",
    dependencies = "mason.nvim",
    cmd = { "DapInstall", "DapUninstall" },
    opts = {
      automatic_installation = true,
      handlers = {},
      ensure_installed = {
        "python",
        "delve", -- Go
        "node2", -- JavaScript
        "codelldb", -- Rust/C/C++
      },
    },
  },
}